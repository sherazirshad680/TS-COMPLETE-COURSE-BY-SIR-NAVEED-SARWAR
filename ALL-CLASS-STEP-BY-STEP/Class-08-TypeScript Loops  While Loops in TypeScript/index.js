"use strict";
// TypeScript Loops | While Loops in TypeScript
// Loops
// For Loop
for (let i = 0; i < 5; i++) {
    console.log(1);
}
// While Loop
let i = 0;
while (i < 5) {
    console.log(i);
    i++;
}
// Write a ts program that prints the table of 2
let start = 1;
while (start <= 10) {
    let result = 2 * start;
    console.log(`2 * ${start} = ${result} `);
    start++;
}
